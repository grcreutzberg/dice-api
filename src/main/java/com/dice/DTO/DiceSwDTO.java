package com.dice.DTO;

public class DiceSwDTO {
    private int habilidade = 0;
    private int proficiencia = 0;
    private int dificuldade = 0;
    private int desafio = 0;
    private int ampliacao = 0;
    private int contratempo = 0;

    public int getHabilidade() {
        return habilidade;
    }

    public void setHabilidade(final int habilidade) {
        this.habilidade = habilidade;
    }

    public int getProficiencia() {
        return proficiencia;
    }

    public void setProficiencia(final int proficiencia) {
        this.proficiencia = proficiencia;
    }

    public int getDificuldade() {
        return dificuldade;
    }

    public void setDificuldade(final int dificuldade) {
        this.dificuldade = dificuldade;
    }

    public int getDesafio() {
        return desafio;
    }

    public void setDesafio(final int desafio) {
        this.desafio = desafio;
    }

    public int getAmpliacao() {
        return ampliacao;
    }

    public void setAmpliacao(final int ampliacao) {
        this.ampliacao = ampliacao;
    }

    public int getContratempo() {
        return contratempo;
    }

    public void setContratempo(final int contratempo) {
        this.contratempo = contratempo;
    }

}
